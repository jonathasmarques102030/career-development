function abrirModalLogin() {
    var modalSobre = document.getElementById("modal-login");
    modalSobre === null || modalSobre === void 0 ? void 0 : modalSobre.style.display = "block";
}
;
function fecharModalLogin() {
    var modalSobre = document.getElementById("modal-login");
    modalSobre === null || modalSobre === void 0 ? void 0 : modalSobre.style.display = 'none';
}
;
